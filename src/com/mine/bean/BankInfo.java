package com.mine.bean;

public class BankInfo {
private String id;
private String myBankKind;
private String myBankName;
private String myBankCard;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getMybankkind() {
	return myBankKind;
}
public void setMybankkind(String mybankkind) {
	this.myBankKind = mybankkind;
}
public String getMybankname() {
	return myBankName;
}
public void setMybankname(String mybankname) {
	this.myBankName = mybankname;
}
public String getMybankcard() {
	return myBankCard;
}
public void setMybankcard(String mybankcard) {
	this.myBankCard = mybankcard;
}

}
